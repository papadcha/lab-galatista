-- ============================================================
--  MIGRATION 015 — Επαναδιόρθωση κατηγορίας 3Α/Ε4 σε ALL_IN
--  Ημερομηνία: 2026-07-03
--  Αναιρέσιμη με: UPDATE tbl_products SET category='ΧΟΝΔΡΟΚΟΚΚΟ' WHERE code IN ('3Α','Ε4');
-- ============================================================
--
--  Πρόβλημα: το migration_001 (2026-05) επαναδιόρθωνε τα 3Α/Ε4 σε
--  category='ALL_IN' με WHERE id IN (4,5) — αλλά initialize_database()
--  σε ΚΑΙΝΟΥΡΙΑ βάση τρέχει ΜΟΝΟ το schema.sql και σφραγίζει κατευθείαν
--  το tbl_schema_version στο CURRENT_SCHEMA_VERSION, χωρίς να περνάει
--  ποτέ από τα migrations. Το schema.sql seed είχε ακόμα hardcoded
--  category='ΧΟΝΔΡΟΚΟΚΚΟ' μέχρι το v1.1.18 — άρα κάθε βάση που
--  δημιουργήθηκε φρέσκια ΠΡΙΝ το v1.1.18 "γεννήθηκε" μόνιμα με λάθος
--  κατηγορία, χωρίς κανένα migration path να το διορθώσει ποτέ
--  αναδρομικά (το migration_001 δεν ξανατρέχει σε ήδη-migrated βάση).
--  Το v1.1.18 διόρθωσε μόνο το seed για ΝΕΕΣ εγκαταστάσεις — καμία
--  ΥΠΑΡΧΟΥΣΑ βάση δεν διορθωνόταν ποτέ αυτόματα.
--
--  Fix: idempotent data-only διόρθωση με WHERE code (πιο ανθεκτικό από
--  hardcoded id), ώστε να "θεραπεύσει" αυτόματα ΚΑΘΕ υπάρχουσα βάση
--  (π.χ. του χειριστή) στο επόμενο αυτόματο migration κατά το άνοιγμα
--  της εφαρμογής, χωρίς να χρειάζεται χειροκίνητη διόρθωση από
--  Ρυθμίσεις → Αδρανή.
-- ============================================================

UPDATE tbl_products
   SET category = 'ALL_IN'
 WHERE code IN ('3Α', 'Ε4')
   AND category != 'ALL_IN';
